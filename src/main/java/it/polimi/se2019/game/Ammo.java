package it.polimi.se2019.game;

public class Ammo {
    private int blue;
    private int red;
    private int yellow;

    public Ammo(){

    }

    public int getBlue() {
        return blue;
    }

    public int getRed() {
        return red;
    }

    public int getYellow() {
        return yellow;
    }

    public void setBlue(int blue) {
        this.blue = blue;
    }

    public void setRed(int red) {
        this.red = red;
    }

    public void setYellow(int yellow) {
        this.yellow = yellow;
    }
}
